/**
 * @brief ADC DMA Half Transfer Complete Callback
 * 
 * Called when DMA has filled the FIRST HALF of the buffer.
 * We can safely process the first half while the second half fills.
 */
void HAL_ADC_ConvHalfCpltCallback(ADC_HandleTypeDef *hadc)
{
    if (hadc->Instance == ADC1)
    {
        /* Process first 32 elements (8 complete scans of 4 channels) */
        ADC_ProcessHalfBuffer(&adc_dma_buffer[0], ADC_DMA_BUFFER_SIZE / 2);
    }
}

/**
 * @brief ADC DMA Full Transfer Complete Callback
 * 
 * Called when DMA has filled the ENTIRE buffer and wrapped to start.
 * We can safely process the second half.
 */
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc)
{
    if (hadc->Instance == ADC1)
    {
        /* Process second 32 elements */
        ADC_ProcessHalfBuffer(&adc_dma_buffer[ADC_DMA_BUFFER_SIZE / 2], ADC_DMA_BUFFER_SIZE / 2);
    }
}

/**
 * @brief Process a half of the DMA buffer
 * 
 * This function averages the multiple samples in the half-buffer
 * to produce a single raw reading per channel.
 * 
 * @param start_ptr Pointer to start of the half-buffer
 * @param count Number of elements in this half
 */
static void ADC_ProcessHalfBuffer(uint16_t *start_ptr, uint16_t count)
{
    uint32_t sum_ch5 = 0, sum_ch6 = 0, sum_ch7 = 0, sum_ch8 = 0;
    uint16_t num_scans = count / ADC_NUM_CHANNELS;
    
    /* Sum all samples for each channel */
    for (uint16_t i = 0; i < num_scans; i++)
    {
        sum_ch5 += start_ptr[(i * ADC_NUM_CHANNELS) + 0]; /* CH5 */
        sum_ch6 += start_ptr[(i * ADC_NUM_CHANNELS) + 1]; /* CH6 */
        sum_ch7 += start_ptr[(i * ADC_NUM_CHANNELS) + 2]; /* CH7 */
        sum_ch8 += start_ptr[(i * ADC_NUM_CHANNELS) + 3]; /* CH8 */
    }
    
    /* Average and store */
    taskENTER_CRITICAL();
    adc_latest_raw.load_cell_raw  = (int16_t)(sum_ch5 / num_scans);
    adc_latest_raw.knee_angle_raw = (int16_t)(sum_ch6 / num_scans);
    adc_latest_raw.moment_raw     = (int16_t)(sum_ch7 / num_scans);
    adc_latest_raw.battery_raw    = (int16_t)(sum_ch8 / num_scans);
    taskEXIT_CRITICAL();
}
