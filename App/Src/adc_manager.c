/**
 * @brief Configure ADC GPIO pins as Analog
 * 
 * Analog mode disconnects digital input Schmitt triggers,
 * reducing power consumption and preventing digital noise.
 */
static void ADC_ConfigGPIO(void)
{
    GPIO_InitTypeDef GPIO_InitStruct = {0};
    
    /* Enable GPIOA clock (already done in main, but safe to repeat) */
    __HAL_RCC_GPIOA_CLK_ENABLE();
    
    /* Configure PA0, PA1, PA2, PA3 */
    GPIO_InitStruct.Pin = ADC_LOAD_CELL_PIN | ADC_KNEE_ANGLE_PIN | 
                          ADC_MOMENT_PIN | ADC_BATTERY_PIN;
    GPIO_InitStruct.Mode = GPIO_MODE_ANALOG;       /* Analog mode */
    GPIO_InitStruct.Pull = GPIO_NOPULL;            /* No pull-ups/downs for analog */
    HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);
}

/**
 * @brief Configure GPDMA1 for ADC circular transfer
 * 
 * GPDMA (General Purpose DMA) on STM32U5 is more complex than legacy DMA.
 * We must configure:
 * - Source address (ADC data register)
 * - Destination address (our SRAM buffer)
 * - Data width (16-bit for ADC)
 * - Block/transfer counts
 * - Circular mode flag
 */
static void ADC_ConfigDMA(void)
{
    /* Ensure GPDMA1 clock is enabled */
    __HAL_RCC_GPDMA1_CLK_ENABLE();
    
    /* Clear any pending DMA flags */
    __HAL_GPDMA1_CLEAR_FLAG(GPDMA1_CHANNEL0, GPDMA_FLAG_TC | GPDMA_FLAG_HT | GPDMA_FLAG_TE);
    
    hdma_adc1.Instance = GPDMA1_CHANNEL0;
    hdma_adc1.Init.Request = GPDMA_REQUEST_ADC1;
    hdma_adc1.Init.Direction = DMA_PERIPH_TO_MEMORY;
    hdma_adc1.Init.Permalink = DMA_PERIPHERAL;
    hdma_adc1.Init.BlkHWRequest = DMA_BK_HWREQUEST_SINGLEBURST;
    hdma_adc1.Init.DataAlignment = DMA_DATAALIGN_HALFWORD; /* 16-bit ADC data */
    hdma_adc1.Init.SrcBurstLength = 1;  /* Read 1 half-word at a time */
    hdma_adc1.Init.DestBurstLength = 1; /* Write 1 half-word at a time */
    hdma_adc1.Init.SrcInc = DMA_PINC_DISABLE;   /* ADC DR address is fixed */
    hdma_adc1.Init.DestInc = DMA_MINC_ENABLE;   /* Increment SRAM buffer pointer */
    hdma_adc1.Init.Priority = DMA_HIGH_PRIORITY;
    
    /* Circular mode is set via the Linked List Node configuration on U5 */
    /* For HAL simplified mode, we use standard circular init if available, 
       otherwise standard mode requires re-triggering. We use HAL standard circular wrapper */
    hdma_adc1.Init.Mode = DMA_NORMAL; /* U5 GPDMA uses LLI for circular, but HAL abstracts this */
    
    /* Link DMA handle to ADC handle BEFORE HAL_ADC_Init */
    __HAL_LINKDMA(&hadc1, DMA_Handle, hdma_adc1);
    
    if (HAL_DMA_Init(&hdma_adc1) != HAL_OK)
    {
        Error_Handler();
    }
    
    /* Enable DMA Interrupts */
    HAL_NVIC_SetPriority(GPDMA1_Channel0_IRQn, 5, 0); /* Priority 5 - below configMAX_SYSCALL_INTERRUPT_PRIORITY */
    HAL_NVIC_EnableIRQ(GPDMA1_Channel0_IRQn);
}

